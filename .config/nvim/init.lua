vim.opt.number = true
vim.opt.smartindent = true
vim.opt.tabstop = 4
vim.opt.shiftwidth = 4
vim.opt.expandtab = true
vim.opt.mouse = 'a'

vim.api.nvim_command [[colorscheme codedark]]

require('plugins')

require'nvim-treesitter.configs'.setup {
	ensure_installed = {"c"},
	highlight = {
		enable = true
	},
	indent = {
		enable = true
	}
